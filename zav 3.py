"""Описати функцію RadToDeg (R) дійсного типу, яка знаходить величину кута в
градусах, якщо дана його величина R в радіанах (R - дійсне число, 0 ≤ R <2 · π).
Скористатися таким співвідношенням: 180 ° = π радіанів. Як значення π вважати
рівним 3.14. За допомогою функції RadToDeg перевести з радіанів в градуси п'ять
даних кутів."""


def RadToDeg(r):
    return r/3.14*180

for i in range(5):
    r = float(input())
    print(RadToDeg(r))
exit(1)